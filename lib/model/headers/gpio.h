#ifndef WBUDY_STM32_SNAKE_GPIO_H
#define WBUDY_STM32_SNAKE_GPIO_H

//OUTPUT MODE
#define GPIO_OUTPUT_GENERAL_PUSH_PULL 0b0000
#define GPIO_OUTPUT_GENERAL_OPEN_DRAIN 0b0100
#define GPIO_OUTPUT_ALTERNATE_PUSH_PULL 0b1000
#define GPIO_OUTPUT_ALTERNATE_OPEN_DRAIN 0b1100

//INPUT MODE
#define GPIO_INPUT_ANALOG 0b0000
#define GPIO_INPUT_FLOATING 0b0100
#define GPIO_INPUT_PULL_UP_PULL_DOWN 0b1000

//MODE
#define GPIO_MODE_INPUT_MODE 0b0000
#define GPIO_MODE_10MHZ 0b0001
#define GPIO_MODE_2MHZ 0b0010
#define GPIO_MODE_50MHZ 0b0011



#endif //WBUDY_STM32_SNAKE_GPIO_H
